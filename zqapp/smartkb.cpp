wchar_t FirstLetter(wchar_t nCode)
{
	if ((nCode >= L'£°' && nCode <= L'£¹'))
	{
		return (L'0' + (nCode - L'£°'));
	}
	else if (nCode >= L'£Á' && nCode <= L'£Ú')
	{
		return (L'A' + (nCode - L'£Á'));
	}
	else if (nCode >= L'£á' && nCode <= L'£ú')
	{
		return (L'A' + (nCode - L'£á'));
	}
	else if (nCode >= L'a' && nCode <= L'z')
	{
		return (L'A' + (nCode - L'a'));
	}
	else if (nCode >= L'A' && nCode <= L'Z')
	{
		return (nCode);
	}
	else if (int(nCode) > 19968 && int(nCode) <= 40869)
	{
		for (size_t j = 0; j < _countof(g_TxtToPy); j++)
		{
			if (g_TxtToPy[j].find(nCode) != g_TxtToPy[j].npos)
			{
				return (g_TxtToPy[j][0]);
				break;
			}
		}
	}
	return nCode;
}

